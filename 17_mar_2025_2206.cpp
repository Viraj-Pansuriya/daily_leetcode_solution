class Solution {
    public:
        bool divideArray(vector<int>& nums) {
            
            vector < int > store(501 , 0); 
            for(auto & x : nums) store[x]++;
    
            for(auto & x : store){
                if(x & 1) return false; 
            }
            return true; 
        }
    };